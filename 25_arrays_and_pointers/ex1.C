#include <stdio.h>

int main() {
    // Declaring an array of 5 integers
    int numbers[5];
    
    // Initializing array elements
    numbers[0] = 10;
    numbers[1] = 20;
    numbers[2] = 30;
    numbers[3] = 40;
    numbers[4] = 50;
    
    // Accessing array elements
    printf("First element: %d\n", numbers[0]);
    printf("Third element: %d\n", numbers[2]);
    
    return 0;
}
